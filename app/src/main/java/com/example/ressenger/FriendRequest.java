package com.example.ressenger;

public class FriendRequest {

    public String name;
    public String time;
    public String type;
    public String uid;

    public FriendRequest(String name, String time, String type, String uid)
    {
        this.name = name;
        this.time = time;
        this.type = type;
        this.uid = uid;
    }

}
